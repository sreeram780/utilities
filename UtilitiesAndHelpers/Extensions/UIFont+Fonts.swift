import Foundation
import UIKit

extension UIFont
{
    class func L36() -> UIFont
    {
        return UIFont.systemFont(ofSize: 36.0, weight: UIFont.Weight.light)
    }
    
    class func M26() -> UIFont
    {
        return UIFont.systemFont(ofSize: 26.0, weight: UIFont.Weight.medium)
    }
    
    class func L26() -> UIFont
    {
        return UIFont.systemFont(ofSize: 26.0, weight: UIFont.Weight.light)
    }
    
    class func M20() -> UIFont
    {
        return UIFont.systemFont(ofSize: 20.0, weight: UIFont.Weight.medium)
    }
}
